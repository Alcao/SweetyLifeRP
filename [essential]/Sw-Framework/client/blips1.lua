function AddBlip(value)
    local blip = AddBlipForCoord(value.x, value.y, value.z)

    if (value.blips.id) then
        SetBlipSprite(blip, value.blips.id)
    end
    if (value.blips.colors) then
        SetBlipColour(blip, value.blips.colors)
    end
    if not (value.blips.short_range) then
        SetBlipAsShortRange(blip, true)
    end
    if (value.blips.name) then
        BeginTextCommandSetBlipName("STRING")
        AddTextComponentSubstringPlayerName(value.blips.name)
        EndTextCommandSetBlipName(blip)
        SetBlipInfoTitle(blip, value.blips.name, false)
    end

    if (value.blips.optional ~= nil) and (value.blips.optional.picture ~= nil) and (value.blips.optional.picture[1] and value.blips.optional.picture[2]) then
        RequestStreamedTextureDict(value.blips.optional.picture[1], 1)
        while not HasStreamedTextureDictLoaded(value.blips.optional.picture[1]) do
            Wait(5000)
        end
        SetBlipInfoImage(blip, value.blips.optional.picture[1], value.blips.optional.picture[2])
    end

    if (type(value.blips.optional.text) == "table") then
        for k, v in pairs(value.blips.optional.text) do
            AddBlipInfoText(blip, v[1], v[2])
            if (v.separator) then
                AddBlipInfoHeader(blip, "")
            end
        end
    end

    if (value.blips.optional.description) then
        AddBlipInfoText(blip, value.blips.optional.description)
    end
    value.blip = blip;
end



